public class AssOpr {
	public static void main(String[] args) {
		int a=2;
		System.out.println("a: "+a);
		
		a+=5;
		System.out.println("a += 2: "+a);
		
		a-=3;
		System.out.println("a -= 2: "+a);
		
		a*=9;
		System.out.println("a *= 2: "+a);
		
		a/=2;
		System.out.println("a /= 2: "+a);
	}
}
